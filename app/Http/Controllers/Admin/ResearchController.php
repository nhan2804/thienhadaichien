    {
         $d = new Research;
        $d->name_r= $r->name;
        $d->desc_r= $r->desc;
        $d->id_re_r= $r->type;
        $d->time_build= $r->time;
        $d->value_r= $r->v;
        $d->save();
        return back();
    }

